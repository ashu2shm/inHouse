<p>  Kazan University was founded on November 17, 1804, 
when Emperor Alexander I signed the Affirmative Letter and the 
Charter about the creation of the Kazan Imperial University. The first 
students, enrolled in 1805, were graduates of the First Kazan 
Gymnasium – an autonomous affiliate of Moscow State University, 
under whose auspices Kazan University first operated.It was not until 
1814 that the university underwent its full opening. It was restructured 
as a classical university comprising four departments: moral and 
political sciences, physical and mathematical sciences, medical 
sciences and philology. Before Tomsk University was founded, the 
University of Kazan used to be the easternmost university in the 
Russian Empire, it was thus serving the Volga, Kama, and Ural 
regions, Siberia and the Caucasus. 
