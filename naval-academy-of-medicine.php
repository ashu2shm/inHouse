       <p>Established in 1940, Naval Academy of Medicine  is a premier medical 
educational Institution in Russia offering high-quality education to local 
citizens or foreign students who come from other countries to study 
MBBS at Naval Academy of Medicine.</p>
<p> Naval Academy of Medicine  is recognized by the prominent 
International organization WHO (World Health Organization) and is 
also recognized by National Medical Commission (NMC) of India 
which ensures that the MBBS degree of Naval Academy of Medicine  
is globally valid. </p><p>The S. M. Kirov Military Medical Academy is the oldest 
school of military medicine in Saint Petersburg and the Russian 
Federation. Senior medical staff are trained for the Armed Forces and 
conduct research in military medical services. 
After Sergey Kirov’s assassination in 1934, the Academy received his 
name. Leon Orbeli, one of Pavlov’s disciples, led the Academy in 1943 
— 1950.</p>
<p>In 1956 S. M. Kirov Military Medical Academy was united with the Naval 
Medical Academy established on the basis of Obukhovskaya Hospital 
and the Third Leningrad Medical Institute in 1940. The academy had 
six faculties, 61 departments, 30 clinics, 16 research laboratories, and 
two research centers in 2002.</p>
<p>Later in 2011, minister of defense Anatoliy Serdyukov declared his 
intention to move the academy from the historical center of Saint 
Petersburg to one of its suburbs. This decision was overturned after 
Serdyukov had been sacked.</p>


